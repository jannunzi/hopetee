(function () {
  function apply(lang) {
    window.__lang = lang;
    document.documentElement.lang = lang;
    const dict = window.HT_I18N[lang] || window.HT_I18N.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] != null) el.innerHTML = dict[key];
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const key = el.getAttribute('data-i18n-ph');
      if (dict[key] != null) el.setAttribute('placeholder', dict[key]);
    });
    document.querySelectorAll('.lang button').forEach(b =>
      b.classList.toggle('active', b.getAttribute('data-lang') === lang));
    document.dispatchEvent(new CustomEvent('ht-lang', { detail: lang }));
  }
  let saved = 'en';
  try { saved = localStorage.getItem('ht_lang') || 'en'; } catch (e) {}
  window.HT_setLang = function (lang) {
    try { localStorage.setItem('ht_lang', lang); } catch (e) {}
    apply(lang);
  };
  document.addEventListener('DOMContentLoaded', function () {
    document.querySelectorAll('.lang button').forEach(b =>
      b.addEventListener('click', () => window.HT_setLang(b.getAttribute('data-lang'))));
    apply(saved);
  });
})();
